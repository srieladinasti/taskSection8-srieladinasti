fun main() {
    tambah(3)
    kurang(5)
    kali(15)
    bagi(18.0)
    hasil(20)
}

fun tambah(t: Int){
    val tambah1 = t + 5
    println(tambah1)
}

fun kurang(k: Int){
    val kurang1 = k - 5
    println(kurang1)
}

fun kali(ka: Int){
    val kali1 = ka * 5
    println(kali1)
}

fun bagi(b: Double){
    val bagi1 = b / 5.0
    println(bagi1)
}

fun hasil(h: Int){
    val hasil1 = h % 5
    println(hasil1)
}